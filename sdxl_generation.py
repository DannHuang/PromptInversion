def main(device):

    args = parse_args()

    ##### 1. Init modules #####
    cross_attn_init()
    ###########################

    pipe = StableDiffusionXLPipeline.from_pretrained(
        args.pretrained_model_name_or_path,
        revision=args.revision,
        variant=args.variant,
    ).to(device)
    print(hasattr(pipe.vae.config, "latents_mean"), pipe.vae.config.latents_mean)
    print(hasattr(pipe.vae.config, "latents_std"), pipe.vae.config.latents_std)

    ##### 2. Replace modules and Register hook #####
    pipe.unet = set_layer_with_name_and_path(pipe.unet)
    pipe.unet = register_cross_attention_hook(pipe.unet)
    ################################################

    height = 1024
    width = 1024
    prompt = "A group of penguins standing on an icy shore, with snowy mountains and a clear blue sky in the background, some penguins diving into the water."
    generator = torch.Generator(device=device).manual_seed(args.seed)
    image = pipe(
        prompt,
        height=height,
        width=width,
        num_inference_steps=30,
        generator=generator,
        # guidance_scale=1.0,
    ).images[0]
    image.save('test.png')

    ##### 3. Process and Save attention map #####
    print('resizing and saving ...')

    ##### 3-1. save by timesteps and path (2~3 minutes) #####
    attn_map = preprocess(max_height=height, max_width=width,)
    visualize_and_save_attn_map(attn_map, pipe.tokenizer_2, [prompt], postfix=f"gt")
    #########################################################


if __name__ == "__main__":
    device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
    main(device)